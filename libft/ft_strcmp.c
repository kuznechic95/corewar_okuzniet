#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	unsigned char t1;
	unsigned char t2;

	while (*s1 == *s2 && *s1 != 0)
	{
		s1++;
		s2++;
	}
	t1 = *s1;
	t2 = *s2;
	return (t1 - t2);
}

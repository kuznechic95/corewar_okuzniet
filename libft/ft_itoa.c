#include "libft.h"

char		*ft_itoa(int n)
{
	int				i;
	char			*str;
	unsigned int	x;

	i = 1;
	x = (n < 0) ? -n : n;
	while (x >= 10 && i++)
		x = x / 10;
	i = (n < 0) ? i + 1 : i;
	if (!(str = (char*)malloc(i + 1)))
		return (0);
	str[i] = '\0';
	x = (n < 0) ? -n : n;
	while (i >= 0)
	{
		str[i - 1] = x % 10 + 48;
		x = x / 10;
		i--;
	}
	str[0] = (n < 0) ? '-' : str[0];
	return (str);
}

#include "corewar.h"

int		is_natural(char *s)
{
	int	i;

	if (!s)
		return (0);
	i = (s[0] == '+') ? 1 : 0;
	while (s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			return (0);
		i++;
	}
	return (1);
}
